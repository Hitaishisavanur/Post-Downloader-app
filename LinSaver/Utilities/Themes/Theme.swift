

import SwiftUI



struct Theme {
    static let lightBackgroundGradient = Gradient(colors: [
        Color(hex: "#faffd1"),
Color(hex: "#a1ffce")
            ])
    static let darkBackgroundGradient = Gradient(colors: [
        Color.black,
        Color.purple.opacity(0.8)
    ])
    
    static let primaryTextColor = Color.primary
    static let secondaryTextColor = Color.secondary
    static let accentColor = Color.blue
    static let deleteColor = Color.red
}

extension Color {
    init(hex: String) {
        let hex = hex.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
        var int: UInt64 = 0
        Scanner(string: hex).scanHexInt64(&int)
        let a, r, g, b: UInt64
        switch hex.count {
        case 3: // RGB (12-bit)
            (a, r, g, b) = (255, (int >> 8 * 17), (int >> 4 & 0xF) * 17, (int & 0xF) * 17)
        case 6: // RGB (24-bit)
            (a, r, g, b) = (255, int >> 16, int >> 8 & 0xFF, int & 0xFF)
        case 8: // ARGB (32-bit)
            (a, r, g, b) = (int >> 24, int >> 16 & 0xFF, int >> 8 & 0xFF, int & 0xFF)
        default:
            (a, r, g, b) = (255, 0, 0, 0)
        }
        self.init(
            .sRGB,
            red: Double(r) / 255,
            green: Double(g) / 255,
            blue: Double(b) / 255,
            opacity: Double(a) / 255
        )
    }
}

